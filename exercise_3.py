# Пользователь вводит месяц в виде целого числа от 1 до 12. Сообщить к какому
# времени года относится месяц (зима, весна, лето, осень).
# Напишите решения через list и через dict.


list_month = ['январь', 'февраль', 'март', 'апрель', 'май', 'июнь', 'июль', 'август',
              'сентябрь', 'октябрь', 'ноябрь', 'декабрь']
dict_month = {1: 'январь', 2: 'февраль', 3: 'март', 4: 'апрель', 5: 'май', 6: 'июнь', 7: 'июль',
              8: 'август', 9: 'сентябрь', 10: 'октябрь', 11: 'ноябрь', 12: 'декабрь'}
try:
    num = abs(int(input('Введите номер месяца: ')))
    if num > 12 or not num:
        print('Введите правильное число!')
    else:
        print(f'{num} месяц - это {list_month[num-1]}.')
        print(f'{num} месяц - это {dict_month[num]}.')
except ValueError:
    print('Вводите только целые числа!')
